    private void orderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderButtonActionPerformed
        Menu menu = Database.menuMap.get(menuKey);

        setAmount(amount);
        
        tempOrders.menus.add(menu);
        tempOrders.totals.add(total);
        tempOrders.discounted.add(discounted);
        tempOrders.types.add(menuType);
        tempOrders.amounts.add(amount);
        
        orderedKeys.add(menuKey);
        orderButton.setEnabled(false);
        amountInc.setEnabled(false);
        amountDec.setEnabled(false);
    }//GEN-LAST:event_orderButtonActionPerformed

