    void setAmount(int amount) {
        this.amount = Math.max(amount, 1);
        amountField.setText("" + this.amount);
        
        double rawPrice = Database.menuMap.containsKey(menuKey) ? (Database.menuMap.get(menuKey).price[menuType] * this.amount) : 0;
        discounted = ((categoryID == 1 && Database.instance.order.member != null) ? rawPrice * 0.05 : 0);
        total = ((categoryID == 1 && Database.instance.order.member != null) ? rawPrice * 0.95 : rawPrice);
        
        totalField.setText("฿ %.2f".formatted(total));
        totalField1.setText("฿ %.2f".formatted(discounted));
    }
    
