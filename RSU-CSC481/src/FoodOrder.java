    private void categorySelectorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_categorySelectorActionPerformed
        categoryID = Math.floorMod(categoryID, Database.categories.size()) + 1;
        
        menuNameModel.removeAllElements();
        for (Menu menu : Database.menus) {
            if (menu.category.id == categoryID)
                menuNameModel.addElement(menu.name);
        }
        
        menuSelector.setSelectedIndex(0);
        
        switch (categoryID) {
            case 1 -> { categorySelector.setIcon(Utility.getImageLocal(getClass(), "/data/MainDish.png", categorySelector.getWidth() - 15, categorySelector.getHeight() - 15));  }
            case 2 -> { categorySelector.setIcon(Utility.getImageLocal(getClass(), "/data/SubDish.png", categorySelector.getWidth() - 15, categorySelector.getHeight() - 15)); }
            case 3 -> { categorySelector.setIcon(Utility.getImageLocal(getClass(), "/data/Salad.png", categorySelector.getWidth() - 15, categorySelector.getHeight() - 15)); }
            case 4 -> { categorySelector.setIcon(Utility.getImageLocal(getClass(), "/data/Drink.png", categorySelector.getWidth() - 15, categorySelector.getHeight() - 15)); }
        }
    }//GEN-LAST:event_categorySelectorActionPerformed

    private void menuSelectorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuSelectorActionPerformed
        menuID = Math.max(menuSelector.getSelectedIndex(), 0) + 1;
        menuKey = Menu.getUID(categoryID, menuID);

        menuType = 0;
        menuTypeModel.removeAllElements();
        for (double price : Database.menuMap.get(menuKey).price) {
            menuTypeModel.addElement("฿ %.2f".formatted(price));
        }
        
        menuPriceSelector.setSelectedIndex(0);
        setAmount(1);
        
        orderButton.setEnabled(!orderedKeys.contains(menuKey));
        amountInc.setEnabled(!orderedKeys.contains(menuKey));
        amountDec.setEnabled(!orderedKeys.contains(menuKey));
        if (orderedKeys.contains(menuKey))
            amountField.setText("" + tempOrders.amounts.get(orderedKeys.indexOf(menuKey)));
        
        menuPreview.setIcon(Utility.getImageFromURL(Database.menuMap.get(menuKey).pictureURL, menuPreview.getWidth(), menuPreview.getHeight()));
    }//GEN-LAST:event_menuSelectorActionPerformed

