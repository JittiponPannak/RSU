    public FrameOrder() {
        initComponents();
        foodTableModel.addTableModelListener(new TableModelListener() {
            boolean stackOverflowFixer = false;
            
            @Override public void tableChanged(TableModelEvent e) {
                if (stackOverflowFixer) return;
                
                stackOverflowFixer = true;
                for (int i = 0; i < foodTableModel.getRowCount(); i++) {
                    if (subOrders.size() > i) {
                        SubOrder sO = subOrders.get(i);
                        sO.amount = (int) foodTableModel.getValueAt(i, 2);
                        foodTableModel.setValueAt(sO.amount * sO.food.price, i, 4);
                    }
                }
                refresh(false);
                stackOverflowFixer = false;
            }
        });
        foodListModel.addAll(Main.instance.foods.keySet());
    }
    public FrameOrder(String table) {
        this();
        this.table = Main.instance.tables.get(table);
        tableLabel.setText("Table : %s".formatted(table));
        refresh(true) ;
    }

    void refresh(boolean updateTable) {
         if (updateTable) {
            foodTableModel.setRowCount(0);
            for (SubOrder sO : subOrders) {
                foodTableModel.addRow(new Object[] { sO.food.name, sO.food.price ,
                                            sO.amount, sO.discount, sO.food.price * sO.amount });
            }
         }
         
         double total = 0.0f ;
        for (SubOrder sO : subOrders)
            total += sO.food.price * sO.amount;
        
        totalField.setText("฿ %.0f".formatted(total));
    }
    
