    private void proceedButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proceedButtonActionPerformed
        if (!subOrders.isEmpty()) {
            Order order = new Order();
            
            order.id = Main.instance.orderIDRecent++;
            order.table = table;
            
            for (SubOrder sO : subOrders) {
                order.unServedorders.push(sO);
                Main.instance.orderPrepareQueue.add(sO);
            }
            
            Main.instance.orders.put(table, order);
            Main.instance.availableTable.remove(table);
            
            
            dispose();
        }
    }//GEN-LAST:event_proceedButtonActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        int index = foodList.getSelectedIndex();
        if (index >= 0 && !addedList.contains(index)) {
            SubOrder sO = new SubOrder();
            
            sO.food = Main.instance.foods.get(foodList.getSelectedValue());
            sO.amount = 1;
            sO.table = table;
            
            subOrders.add(sO);
            addedList.add(index);
            
            refresh(true);
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        int index = foodOrderTable.getSelectedRow();
        if (index >= 0) {
            subOrders.remove(index);
            addedList.remove(index);
            refresh(true);
        }
    }//GEN-LAST:event_removeButtonActionPerformed

