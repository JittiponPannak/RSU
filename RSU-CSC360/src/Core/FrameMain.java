    void refresh() {
        reserveButton.setEnabled(Main.instance.availableTable.isEmpty());
        
        tableTableModel.setRowCount(0);
        for (Table table : Main.instance.tables.values()) {
            String occupier = Main.instance.availableTable.contains(table) ? "Available" : "" + Main.instance.orders.get(table).id;
            tableTableModel.addRow(new Object[] { table.id, table.seats, occupier });
        }
        
        orderPrepareModel.removeAllElements();
        for (SubOrder sO : Main.instance.orderPrepareQueue)
            orderPrepareModel.addElement(sO.table.id + " - " + sO.food.name);
        
        orderReadyModel.removeAllElements();
        for (SubOrder sO : Main.instance.orderServingQueue)
            orderReadyModel.addElement(sO.table.id + " - " + sO.food.name);
        
    }
    
