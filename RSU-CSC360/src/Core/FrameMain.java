    private void orderListToLeftButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderListToLeftButtonActionPerformed
        int selectedIndex = orderReadyList.getSelectedIndex();
        if (selectedIndex >= 0) {
            SubOrder sO = Main.instance.orderServingQueue.remove(selectedIndex);
            Main.instance.orderPrepareQueue.push(sO);
            refresh();
        }
    }//GEN-LAST:event_orderListToLeftButtonActionPerformed

    private void orderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderButtonActionPerformed
        int selectedRow = tableTable.getSelectedRow();
        if (selectedRow >= 0) {
            FrameManager.instance.next(new FrameOrder((String) tableTable.getValueAt(selectedRow, 0)));
        }
    }//GEN-LAST:event_orderButtonActionPerformed

    private void orderListCancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderListCancelButtonActionPerformed
        int selectedIndex;
        
        selectedIndex = orderPreparingList.getSelectedIndex();
        if (selectedIndex >= 0) {
            Main.instance.cancelSubOrder(Main.instance.orderPrepareQueue.get(selectedIndex));
            refresh();
        }
        
        selectedIndex = orderReadyList.getSelectedIndex();
        if (selectedIndex >= 0) {
            Main.instance.cancelSubOrder(Main.instance.orderServingQueue.get(selectedIndex));
            refresh();
        }
    }//GEN-LAST:event_orderListCancelButtonActionPerformed

    private void orderListToRightButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderListToRightButtonActionPerformed
        int selectedIndex = orderPreparingList.getSelectedIndex();
        if (selectedIndex >= 0) {
            SubOrder sO = Main.instance.orderPrepareQueue.remove(selectedIndex);
            Main.instance.orderServingQueue.push(sO);
            refresh();
        }
    }//GEN-LAST:event_orderListToRightButtonActionPerformed

    private void orderButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderButton1ActionPerformed
        int selectedRow = tableTable.getSelectedRow();
        if (selectedRow >= 0) {
            String tbName = (String) tableTable.getValueAt(selectedRow, 0);
            Table table = Main.instance.tables.get(tbName);
            
            if (!Main.instance.availableTable.contains(table))
                FrameManager.instance.next(new FrameFinalize(tbName));
        }
    }//GEN-LAST:event_orderButton1ActionPerformed

    private void orderListServeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderListServeButtonActionPerformed
        int selectedIndex = orderReadyList.getSelectedIndex();
        if (selectedIndex >= 0) {
            SubOrder sO = Main.instance.orderServingQueue.remove(selectedIndex);
            Main.instance.serveSubOrder(sO);
            refresh();
        }
    }//GEN-LAST:event_orderListServeButtonActionPerformed

